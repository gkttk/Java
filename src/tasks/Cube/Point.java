package tasks.Cube;

public class Point {

    private double x,y,z;

    public Point(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }


    public double getY() {
        return y;
    }

    public double getX() {
        return x;
    }

    public double getZ() {
        return z;
    }
}
